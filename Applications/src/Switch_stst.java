public class Switch_stst {
    public static void main(String[] args) {
        String day = "Pizza";
        switch(day){
            case "Sunday": System.out.println("It is Sunday");
                break;
            case "Monday": System.out.println("It is Mon");
                break;
            case "Tuesday": System.out.println("It is Tues");
                break;
            case "Wednesday": System.out.println("It is Wed");
                break;
            case "Thursday": System.out.println("It is Thu");
                break;
            case "Friday": System.out.println("It is Fri");
                break;
            case "Saturday": System.out.println("It is Sat");
                break;
            default: System.out.println("That is not a day");
            break;

        }
    }
}
