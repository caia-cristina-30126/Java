public class Wrapper {
    public static void main(String[] args) {
        //wrapper class =  a way to use primitive data types to reference data types ( that have useful methods)

        //autoboxing
        Boolean a = true;
        Character b = '@';
        Integer c = 2;
        String e = "Bro";

        //unboxing = you can still use the data types as primitive even they are reference
        if(a == true){
            System.out.println("Tris is true");
        }

    }
}
